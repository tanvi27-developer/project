public class FindVowels {
    public static void main(String[]args){
        char a[]={'t','a','n','v','i'};
        for(int i=0;i<a.length;i++){
            if(a[i]=='a'|| a[i]=='e' || a[i]=='i' || a[i]=='o' || a[i]=='u'){
                System.out.println("Vowels are"+" "+ a[i]);
            }
           // System.out.println(a[i]);
        }
    }

    
}
